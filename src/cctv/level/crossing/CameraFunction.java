package cctv.level.crossing;

/**
 * This Enumeration provides constant values that detail the Camera Function.
 * @author Jonathan Moss
 * @version v1.0 December 2016
 */
public enum CameraFunction {
 
    /**
     * Camera one is active.
     */
    CAMERA_ONE,
    
    /**
     * The monitor is not connected to any camera.
     */
    OFF,
    
    /**
     * Camera two is active.
     */
    CAMERA_TWO;
    
}
