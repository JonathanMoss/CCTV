package cctv.level.crossing;

/**
 * This Enumeration provides constant values that represent reminder appliances that can be used on the Level Crossing Controls.
 * @author Jonathan Moss
 * @version v1.0 December 2016
 */
public enum TypeOfReminderAppliance {

    /**
     * A reminder appliance that can be used on a switch.
     */
    SWITCH, 
    
    /**
     * A reminder appliance that can be used on a button.
     */
    BUTTON;
    
}
